public class WhileLoop {
    public static void main(String[] args) {
        
        for(int i = 100; i <= 110; i++){
            System.out.println(i);
        }
        System.out.println("----------------");
        for(int i = 0; i < 5; i++){
            System.out.println("Florin Albu");
        }
        System.out.println("----------------");
        for(int i = 10; i >= 0; i--){
            System.out.println(i);
        }
    }
    
}
