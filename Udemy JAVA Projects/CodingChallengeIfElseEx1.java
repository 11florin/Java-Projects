public class CodingChallengeIfElseEx1 {
    public static void main(String[] args) {
        int x = 91;
        int y = 75;

        if(x > y){
            System.out.println("x is bigger the y " + (x- y));
        }
        else if(x < y){
            System.out.println("x is smaller the y " + (x - y));
        }
        else{
            System.out.println("x and y are the same");
        }
    }
    
}
